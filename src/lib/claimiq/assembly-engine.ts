// ─────────────────────────────────────────────────────────────────────────────
// Types
// ─────────────────────────────────────────────────────────────────────────────

export type SectionStatus = "ready" | "partial" | "missing" | "manual";

export interface SectionReadiness {
  key: string;
  label: string;
  number: number;
  required: boolean;
  status: SectionStatus;
  /** 0-100 — how much data is available for this section */
  completeness: number;
  /** What data sources are available */
  availableSources: string[];
  /** What's still missing */
  missingItems: string[];
  /** Human-readable next action for the pro */
  nextAction: string | null;
  /** Can this section be auto-generated by AI? */
  canAutoGenerate: boolean;
  /** Has this section already been generated? */
  hasExistingContent: boolean;
}

export interface ClaimIQReadiness {
  claimId: string;
  overallScore: number; // 0-100
  overallGrade: "A" | "B" | "C" | "D" | "F";
  sections: SectionReadiness[];
  /** Counts */
  readySections: number;
  partialSections: number;
  missingSections: number;
  /** Actionable summary */
  topActions: string[];
  /** Data layer status */
  layers: {
    vision: { ready: boolean; photoCount: number; detectionCount: number };
    weather: { ready: boolean; hasVerification: boolean };
    documentation: { ready: boolean; generatedDocs: number };
    workflow: { ready: boolean; claimStatus: string };
  };
}

