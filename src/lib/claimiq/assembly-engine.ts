/**
 * ClaimIQ™ — Assembly Engine
 *
 * The "Evidence Orchestrator" — connects the four layers:
 *   1. Computer Vision (YOLO detections)
 *   2. Claim Intelligence (weather + event correlation)
 *   3. Documentation (17-section packet)
 *   4. Contractor Workflow (claim workspace)
 *
 * For each of the 17 sections it determines:
 *   - What data is available
 *   - What's missing
 *   - Whether the section can auto-fill
 *   - What the pro needs to do next
 *
 * This is the brain that turns "upload photos" into "full packet writes itself."
 */

import { logger } from "@/lib/logger";
